import fn from "./insert-interval"
const testcase = [
    [
        [[1, 3], [6, 9]],
        [2, 5],
        [[1, 5], [6, 9]]
    ],
    [
        [[1, 2], [3, 5], [6, 7], [8, 10], [12, 16]],
        [4, 8],
        [[1, 2], [3, 10], [12, 16]]
    ],
    [
        [[2, 3], [6, 9]],
        [0, 1],
        [[0, 1], [2, 3], [6, 9]]
    ],
    [
        [[2, 3], [6, 9]],
        [0, 2],
        [[0, 3], [6, 9]]
    ],
    [
        [[2, 3], [6, 9]],
        [9, 10],
        [[2, 3], [6, 10]]
    ],
    [
        [[2, 3], [6, 9]],
        [11, 12],
        [[2, 3], [6, 9], [11, 12]]
    ],
    [
        [],
        [2, 3],
        [[2, 3]]
    ],
    [
        [[1, 5]],
        [0, 3],
        [[0, 5]]
    ]
];
for (let [a, b, answer] of testcase) {
    test(`${JSON.stringify(a)}, ${JSON.stringify(b)} => ${JSON.stringify(answer)}`, () => {
        expect(fn(a, b)).toStrictEqual(answer);
    });
}